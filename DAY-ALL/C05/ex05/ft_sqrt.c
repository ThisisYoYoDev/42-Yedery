int	ft_sqrt(int nb)
{
	long int y;

	y = 1;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (y * y < nb)
		y++;
	if (y * y == nb)
		return (y);
	else
		return (0);
}

int main(void)
{
	printf ("%d\n", ft_sqrt(2147483647));
}