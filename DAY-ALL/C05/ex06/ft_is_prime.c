int	ft_is_prime(int nb)
{
	int y;

	y = 2;
	if (nb <= 1)
		return (0);
	while (y <= nb / y)
	{
		if (nb % y == 0)
			return (0);
		y++;
	}
	return (1);
}

int main(void)
{
	printf ("%d\n", ft_is_prime(2147483647));
}