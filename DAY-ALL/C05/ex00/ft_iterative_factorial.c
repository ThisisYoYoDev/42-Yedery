int	ft_iterative_factorial(int nb)
{
	int y;
	int z;

	y = 1;
	z = 1;
	if (nb < 0)
		return (0);
	else if (nb == 0)
		return (1);
	while (y <= nb)
	{
		z *= y;
		y++;
	}
	return (z);
}
