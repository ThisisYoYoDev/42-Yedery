int	ft_any(char **tab, int (*f)(char*))
{
	int y;
	int buf_tab;

	y = 0;
	while (tab[y] > 0)
	{
		buf_tab = (*f)(tab[y]);
		if (buf_tab != 0)
			return (1);
		y++;
	}
	return (0);
}
