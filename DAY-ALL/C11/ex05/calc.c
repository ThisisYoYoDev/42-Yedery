#include "ft.h"

long int	ft_add(int a, int b)
{
	return (a + b);
}

long int	ft_sub(int a, int b)
{
	return (a - b);
}

long int	ft_multi(int a, int b)
{
	return (a * b);
}

long int	ft_div(int a, int b)
{
	return (a / b);
}

long int	ft_mod(int a, int b)
{
	return (a % b);
}
