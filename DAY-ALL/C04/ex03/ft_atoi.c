int	ft_atoi(char *str)
{
	int y;
	int nbrneg;

	y = 0;
	nbrneg = 1;
	while ((str[y] == ' ') || (str[y] == '\t')
		|| (str[y] == '\n') || (str[y] == '\v')
		|| (str[y] == '\r') || (str[y] == '\f'))
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			nbrneg = -nbrneg;
		str++;
	}
	while (str[0] >= '0' && str[0] <= '9')
	{
		y = y * 10 + *str - '0';
		str++;
	}
	return (y * nbrneg);
}
