#include <unistd.h>

int main(int y, char **check)
{
	if (y != 2)
		write(1, "a", 1);
	else
	{
		while (*check[1])
		{
			if (*check[1] == 'a')
			{
				write(1, "a", 1);
				break ;
			}
		}
		check[1]++;
	}
	write (1, "\n", 1);
	return 0;
}
