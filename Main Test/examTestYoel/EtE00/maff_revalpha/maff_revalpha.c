#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int a;
	int b;

	a = 'z';
	b = 'Y';
	while(a >= 'b')
	{
		while (b >= 'A')
		{
			ft_putchar(a);
			a += 2;
			ft_putchar(b);
			b += 2;
		}
		a++;
		b++;
	}
}
